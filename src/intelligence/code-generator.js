/**
 * Code Generator - Software 2.0 Implementation
 * Generates and evolves code autonomously
 */

import logger from '../utils/logger.js';

class CodeGenerator {
    constructor(app) {
        this.app = app;
        this.generationHistory = [];
        this.templates = new Map();
        this.codeRepository = new Map();
        
        this.initializeTemplates();
    }

    initializeTemplates() {
        // Define code generation templates
        this.templates.set('api_endpoint', {
            name: 'API Endpoint',
            generator: this.generateApiEndpoint.bind(this)
        });

        this.templates.set('data_model', {
            name: 'Data Model',
            generator: this.generateDataModel.bind(this)
        });

        this.templates.set('ui_component', {
            name: 'UI Component',
            generator: this.generateUiComponent.bind(this)
        });

        this.templates.set('optimization', {
            name: 'Optimization Rule',
            generator: this.generateOptimization.bind(this)
        });
    }

    async generate(spec) {
        logger.info('[CodeGenerator] Generating code for:', spec);

        try {
            // Determine what type of code to generate
            const templateType = this.determineTemplate(spec);
            const template = this.templates.get(templateType);

            if (!template) {
                throw new Error(`No template found for type: ${templateType}`);
            }

            // Generate code
            const generatedCode = await template.generator(spec);

            // Validate generated code
            const validation = await this.validateCode(generatedCode);

            if (!validation.valid) {
                throw new Error(`Generated code validation failed: ${validation.reason}`);
            }

            // Store generated code
            this.storeCode(generatedCode);

            // Record generation
            this.generationHistory.push({
                timestamp: Date.now(),
                type: templateType,
                spec,
                success: true,
                codeId: generatedCode.id
            });

            this.app.logActivity(`Code generated: ${template.name}`);

            return {
                success: true,
                code: generatedCode,
                validation
            };
        } catch (error) {
            logger.error('[CodeGenerator] Generation error:', error);
            
            this.generationHistory.push({
                timestamp: Date.now(),
                spec,
                success: false,
                error: error.message
            });

            return {
                success: false,
                error: error.message
            };
        }
    }

    determineTemplate(spec) {
        // Simple logic to determine template type
        if (spec.type) return spec.type;
        
        if (spec.includes('api') || spec.includes('endpoint')) return 'api_endpoint';
        if (spec.includes('model') || spec.includes('data')) return 'data_model';
        if (spec.includes('component') || spec.includes('ui')) return 'ui_component';
        if (spec.includes('optimize') || spec.includes('performance')) return 'optimization';
        
        return 'optimization'; // Default
    }

    // Code generation implementations

    async generateApiEndpoint(spec) {
        const code = {
            id: this.generateId(),
            type: 'api_endpoint',
            timestamp: Date.now(),
            content: `
/**
 * Auto-generated API Endpoint
 * Generated by S4 Code Generator
 */
async function handleRequest(request) {
    try {
        const data = await request.json();
        
        // Process request
        const result = await processData(data);
        
        return {
            success: true,
            data: result
        };
    } catch (error) {
        return {
            success: false,
            error: error.message
        };
    }
}

async function processData(data) {
    // Processing logic
    return data;
}

export { handleRequest };
            `.trim()
        };

        return code;
    }

    async generateDataModel(spec) {
        const code = {
            id: this.generateId(),
            type: 'data_model',
            timestamp: Date.now(),
            content: `
/**
 * Auto-generated Data Model
 * Generated by S4 Code Generator
 */
class DataModel {
    constructor(data = {}) {
        this.id = data.id || this.generateId();
        this.createdAt = data.createdAt || Date.now();
        this.updatedAt = data.updatedAt || Date.now();
        this.data = data;
    }

    generateId() {
        return 'id_' + Date.now() + '_' + Math.random().toString(36).slice(2, 11);
    }

    toJSON() {
        return {
            id: this.id,
            createdAt: this.createdAt,
            updatedAt: this.updatedAt,
            data: this.data
        };
    }

    static fromJSON(json) {
        return new DataModel(json);
    }
}

export default DataModel;
            `.trim()
        };

        return code;
    }

    async generateUiComponent(spec) {
        const code = {
            id: this.generateId(),
            type: 'ui_component',
            timestamp: Date.now(),
            content: `
/**
 * Auto-generated UI Component
 * Generated by S4 Code Generator
 */
class Component {
    constructor(container) {
        this.container = container;
        this.state = {};
        this.render();
    }

    setState(newState) {
        this.state = { ...this.state, ...newState };
        this.render();
    }

    render() {
        if (!this.container) return;
        
        this.container.innerHTML = \`
            <div class="component">
                <h3>Auto-Generated Component</h3>
                <p>State: \${JSON.stringify(this.state)}</p>
            </div>
        \`;
    }
}

export default Component;
            `.trim()
        };

        return code;
    }

    async generateOptimization(spec) {
        const code = {
            id: this.generateId(),
            type: 'optimization',
            timestamp: Date.now(),
            content: `
/**
 * Auto-generated Optimization Rule
 * Generated by S4 Code Generator
 */
class OptimizationRule {
    constructor() {
        this.name = 'Auto-Generated Optimization';
        this.priority = 1;
    }

    canApply(context) {
        // Check if optimization can be applied
        return context.needsOptimization === true;
    }

    apply(context) {
        logger.info('[Optimization] Applying optimization...');
        
        // Apply optimization logic
        const optimized = {
            ...context,
            optimized: true,
            timestamp: Date.now()
        };

        return optimized;
    }
}

export default OptimizationRule;
            `.trim()
        };

        return code;
    }

    async validateCode(code) {
        // Basic validation
        if (!code || !code.content) {
            return { valid: false, reason: 'No code content' };
        }

        // Check for syntax issues (basic checks)
        const content = code.content;
        
        // Check for balanced braces
        const openBraces = (content.match(/{/g) || []).length;
        const closeBraces = (content.match(/}/g) || []).length;
        
        if (openBraces !== closeBraces) {
            return { valid: false, reason: 'Unbalanced braces' };
        }

        // Check for basic structure
        if (!content.includes('function') && !content.includes('class')) {
            return { valid: false, reason: 'No function or class definition' };
        }

        return { valid: true };
    }

    storeCode(code) {
        this.codeRepository.set(code.id, code);
        
        // Trim repository size
        if (this.codeRepository.size > 100) {
            const firstKey = this.codeRepository.keys().next().value;
            this.codeRepository.delete(firstKey);
        }
    }

    generateId() {
        return 'code_' + Date.now() + '_' + Math.random().toString(36).slice(2, 11);
    }

    getStatus() {
        return {
            active: true,
            generationsCount: this.generationHistory.length,
            codeRepositorySize: this.codeRepository.size,
            successRate: this.calculateSuccessRate()
        };
    }

    calculateSuccessRate() {
        if (this.generationHistory.length === 0) return 100;
        
        const successful = this.generationHistory.filter(h => h.success).length;
        return (successful / this.generationHistory.length * 100).toFixed(2);
    }

    getGeneratedCode(id) {
        return this.codeRepository.get(id);
    }

    getAllGeneratedCode() {
        return Array.from(this.codeRepository.values());
    }

    getGenerationHistory() {
        return this.generationHistory.slice(-20);
    }
}

export default CodeGenerator;
